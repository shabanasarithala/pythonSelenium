print("__________ print 0-10 numbers___________")

for i in range(11):
    print(i)  # 0-10
print("__________ print 1-10 numbers___________")

for i in range(1, 11):
    print(i)  # 1-10

print("__________ print even numbers____________")

for i in range(2, 12, 2):
    print(i)
print("__________ print reverse order 10-1 numbers____________")

for i in range(10, 1, -1):
    print(i)
print("__________ print max of numbers____________")

print(max(1, 2, 3, 4, 5, 66))
print("__________ print min of numbers____________")

print(min(1, 2, 3, 4, 5, 66))

print("__________ print numbers 0,5,10,15...____________")

for i in range(0, 55, 5):
    print(i)
